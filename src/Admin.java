/**
 * Admin
 */
public class Admin {
    private int adminID;
    private String name;
    private String address;
    private String phone;
    private String email;
    private int salary;

    public Admin(int adminID, String name, String address, String phone, String email, int salary){
        this.adminID = adminID;
        this.name = name;
        this.address = address;
        this.phone = phone;
        this.email = email;
        this.salary = salary;
    }
}

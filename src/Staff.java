/**
 * Staff
 */
public class Staff {

    private int staffID;
    private String name;
    private String address;
    private String phone;
    private String email;
    private int salary;

    public Staff(int staffID, String name, String address, String phone, String email, int salary){
        this.staffID = staffID;
        this.name = name;
        this.address = address;
        this.phone = phone;
        this.email = email;
        this.salary = salary;
    }
}
